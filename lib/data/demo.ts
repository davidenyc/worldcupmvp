function intentForBlueprint(blueprint: VenueBlueprint): VenueIntentKey {
  if (blueprint.venueTypes.includes("supporter_club")) return "both";
  if (blueprint.venueTypes.includes("lounge") || blueprint.venueTypes.includes("bar")) return "watch_party";
  if (blueprint.venueTypes.includes("cultural_center")) return "both";
  if (blueprint.venueTypes.includes("bakery") || blueprint.venueTypes.includes("cafe")) return "cultural_dining";
  return blueprint.screenBase >= 6 ? "sports_bar" : "both";
}

function capacityBucketFromValue(value: number): CapacityBucket {
  if (value < 30) return "under_30";
  if (value <= 60) return "30_60";
  if (value <= 100) return "60_100";
  if (value <= 200) return "100_200";
  return "200_plus";
}

function getNeighborhoodFor(countryIndex: number, venueIndex: number) {
  return demoNeighborhoods[(countryIndex * 3 + venueIndex) % demoNeighborhoods.length];
}

function makeAddress(countryIndex: number, venueIndex: number, neighborhood: NeighborhoodSummary) {
  const street = streetPool[(countryIndex + venueIndex * 2) % streetPool.length];
  const building = 40 + countryIndex * 3 + venueIndex * 11;
  const zipBase: Record<BoroughKey, string> = {
    Manhattan: "100",
    Brooklyn: "112",
    Queens: "111",
    Bronx: "104",
    "Staten Island": "103"
  };

  const zip = `${zipBase[neighborhood.borough]}${String((countryIndex + venueIndex) % 10).padStart(2, "0")}`.slice(
    0,
    5
  );

  return `${building} ${street}, New York, NY ${zip}`;
}

function confederationCuisine(confederation: ConfederationKey) {
  if (confederation === "CAF") return ["grill", "tea", "spice-forward"];
  if (confederation === "AFC") return ["street food", "late-night bites", "tea"];
  if (confederation === "CONMEBOL") return ["grill", "pastries", "cocktails"];
  if (confederation === "Concacaf") return ["casual plates", "beer", "watch parties"];
  if (confederation === "UEFA") return ["bistro", "pub fare", "wine"];
  return ["casual plates", "community hall", "watch parties"];
}

